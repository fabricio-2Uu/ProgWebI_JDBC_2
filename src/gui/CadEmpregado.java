    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String endereco = txtEndereco.getText();
        String telefone = txtTelefone.getText();
        long numero = Long.parseLong(txtCpfNumero.getText());
        int digito = Integer.parseInt(txtCpfDigito.getText());
        Cpf cpf = new Cpf(numero, digito);
        Empregado emp = new Empregado(nome, endereco, telefone, cpf);
        if(emp.insert()>0)
            JOptionPane.showMessageDialog(this, "Empregado cadastrado com sucesso!");
        else
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar empregado!");
    }//GEN-LAST:event_btnOkActionPerformed

